def showline(list_data, x_label="", y_label="", title="", label=[]):
    fig, ax = plt.subplots()
    for i in range(len(list_data)):
        data = list_data[i]
        data = np.array(data)
        data = np.around(data,1)
        yy = data[data != 0]
        xx = np.array(list(range(len(data))))[data != 0]
        ax.plot(xx, yy,label=label[i])
    ax.legend( loc='upper left', shadow=True)
    ax.set(xlabel=x_label, ylabel=y_label, title=title)
    ax.grid()

    # fig.savefig("sigma.png")
    plt.show()
